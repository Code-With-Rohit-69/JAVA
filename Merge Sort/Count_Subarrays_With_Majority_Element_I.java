public class Count_Subarrays_With_Majority_Element_I {

    public static void main(String[] args) {
        int[] nums = { 1, 1, 1, 1 };
        int t = 1;

        Solution obj = new Solution();

        int res = obj.countMajoritySubarrays(nums, t);

        System.out.println(res);

        return;

    }
}

class Solution {
    int T;
    int n;

    public int merge(int[] nums, int l, int mid, int r) {
        int count = 0;

        int p = l;

        for (int j = mid + 1; j <= r; j++) {
            while (p <= mid && nums[p] < nums[j]) {
                p++;
            }

            count += (p - l);
        }

        int[] temp = new int[r - l + 1];
        int k = 0;
        int i = l;
        int j = mid + 1;
        
        while (i <= mid && j <= r) {
            if (nums[i] < nums[j]) {
                temp[k++] = nums[i++];
            } else {
                temp[k++] = nums[j++];
            }
        }

        while (i <= mid) {
            temp[k++] = nums[i++];
        }

        while (j <= r) {
            temp[k++] = nums[j++];
        }

        for (k = 0, i = l; k < temp.length; k++, i++) {
            nums[i] = temp[k];
        }

        return count;

    }

    public int mergeSort(int[] nums, int i, int j) {
        if (i >= j)
            return 0;

        int total = 0;

        int mid = i + (j - i) / 2;

        total += mergeSort(nums, i, mid);
        total += mergeSort(nums, mid + 1, j);

        total += merge(nums, i, mid, j);

        return total;

    }

    public int countMajoritySubarrays(int[] nums, int target) {
        T = target;
        n = nums.length;

        int[] pref = new int[n + 1];
        pref[0] = 0;

        for (int i = 0; i < n; i++) {
            int val = (nums[i] == target) ? 1 : -1;
            pref[i + 1] = pref[i] + val;
        }

        return mergeSort(pref, 0, n);

    }
}

